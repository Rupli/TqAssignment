package com.Corejava;

public class BitwiseOpration {

	public static void main (String args[]) 
	
{


int a= 3;

int b=2;

System.out.println("Bitwise opration of AND:"+( a&b) );

System.out.println("Bitwise opration of OR:"+( a|b) );

System.out.println("Bitwise opration of xor:"+( a^b) );

System.out.println("Bitwise opration:"+ (~a) );//negation

System.out.println("Left shift:"+ (12<<3));//left shift

System.out.println("Left shift:"+ (12>>3));//right shift

}
	
}
